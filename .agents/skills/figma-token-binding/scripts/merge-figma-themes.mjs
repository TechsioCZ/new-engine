#!/usr/bin/env node
/*
 * Merge the per-mode Figma exports into Tailwind-consumable files.
 *
 * Inputs (each a `:root { … }` block of raw CSS variables):
 *   libs/ui/src/tokens/figma/light/variables.css      (base brand, light mode)
 *   libs/ui/src/tokens/figma/dark/variables.css       (base brand, dark mode)
 *   libs/ui/src/tokens/figma/neo/variables.css        (neo brand,  light mode)
 *   libs/ui/src/tokens/figma/neo-dark/variables.css   (neo brand,  dark mode)
 *
 * Outputs:
 *   libs/ui/src/tokens/figma/variables.css        (@theme static { … }) — base brand
 *   libs/ui/src/tokens/figma/brand-overrides.css  ([data-theme="…"] { … }) — other brands
 *
 * Per-token merge rule (within a brand, across light/dark):
 *   - Same value in both modes → emit `--name: <value>;`
 *   - Different values         → emit `--name: light-dark(<L>, <D>);`
 *
 * Brand-override rule (a brand vs. the base brand, after merging):
 *   - Only tokens whose merged value DIFFERS from base are emitted, under
 *     `[data-theme="<attr>"]`. Because component/semantic tokens are var()
 *     chains pointing at the primitives, overriding the handful of differing
 *     primitives (and re-aliased semantics) cascades to the whole system.
 *
 * The base file uses `@theme static` so Tailwind v4 auto-generates utilities.
 * `light-dark()` follows the document's `color-scheme`, which _tokens-base.css
 * manages via :is(.dark, .always-dark). The brand-override file is plain
 * (unlayered) CSS imported AFTER variables.css so its declarations win the
 * cascade over the `@theme` values, while still respecting `color-scheme`.
 */

import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..", "..", "..", "..")
const FIGMA_DIR = join(REPO_ROOT, "libs/ui/src/tokens/figma")

const BASE = { light: "light", dark: "dark" }
const VARIABLES_OUT = join(FIGMA_DIR, "variables.css")
const OVERRIDES_OUT = join(FIGMA_DIR, "brand-overrides.css")

/*
 * Brands beyond `base`. Each maps to a `[data-theme="<attr>"]` selector and
 * points at its per-mode export folders. Add a future brand by exporting its
 * folders and appending an entry here. A light-only brand can reuse its light
 * folder for `dark` (its dark control is hidden by the theme config anyway).
 */
const BRANDS = [{ attr: "neo", light: "neo", dark: "neo-dark" }]

const DECL_RE = /^\s*(--[a-z0-9-]+):\s*([^;]+);/gm

function parseDecls(relDir) {
  const css = readFileSync(join(FIGMA_DIR, relDir, "variables.css"), "utf8")
  const out = new Map()
  DECL_RE.lastIndex = 0
  for (const m of css.matchAll(DECL_RE)) {
    out.set(m[1], m[2].trim())
  }
  return out
}

/*
 * Merge a brand's light + dark declarations into one map of
 * name -> "value" | "light-dark(L, D)". Tokens present in only one mode are
 * kept as-is for both.
 */
function mergeMode(lightDecls, darkDecls) {
  const names = [...new Set([...lightDecls.keys(), ...darkDecls.keys()])].sort()
  const merged = new Map()
  for (const name of names) {
    const L = lightDecls.get(name)
    const D = darkDecls.get(name)
    if (L === undefined) merged.set(name, D)
    else if (D === undefined || L === D) merged.set(name, L)
    else merged.set(name, `light-dark(${L}, ${D})`)
  }
  return merged
}

function main() {
  const base = mergeMode(parseDecls(BASE.light), parseDecls(BASE.dark))

  // --- base brand → variables.css (@theme static) ---
  let identical = 0
  let modeDiff = 0
  const v = []
  v.push("/*")
  v.push(" * Unified Figma tokens — base brand, light + dark merged into @theme static.")
  v.push(" * Generated by merge-figma-themes.mjs. DO NOT EDIT BY HAND.")
  v.push(" *")
  v.push(" * - Tokens identical in both modes: single value.")
  v.push(" * - Tokens that differ:            light-dark(L, D).")
  v.push(" *")
  v.push(" * `@theme static` makes Tailwind v4 generate utilities automatically.")
  v.push(" * `light-dark()` follows the document's `color-scheme`, which")
  v.push(" * _tokens-base.css manages via :is(.dark, .always-dark) selectors.")
  v.push(" * Non-base brands live in brand-overrides.css (imported after this).")
  v.push(" */")
  v.push("")
  v.push("@theme static {")
  for (const [name, value] of base) {
    v.push(`  ${name}: ${value};`)
    if (value.startsWith("light-dark(")) modeDiff += 1
    else identical += 1
  }
  v.push("}")
  v.push("")
  writeFileSync(VARIABLES_OUT, v.join("\n"))
  console.log(`✓ wrote ${VARIABLES_OUT}`)
  console.log(`  base: ${identical} identical, ${modeDiff} mode-different (light-dark)`)

  // --- non-base brands → brand-overrides.css ([data-theme] blocks) ---
  const o = []
  o.push("/*")
  o.push(" * Brand theme overrides — non-base brands keyed by [data-theme].")
  o.push(" * Generated by merge-figma-themes.mjs. DO NOT EDIT BY HAND.")
  o.push(" *")
  o.push(" * Only tokens whose merged value differs from the base brand are")
  o.push(" * emitted; everything else inherits base via var() chains. Plain")
  o.push(" * (unlayered) selectors so they win over @theme while still")
  o.push(" * respecting `color-scheme` for the light-dark() halves.")
  o.push(" */")
  o.push("")

  for (const brand of BRANDS) {
    const merged = mergeMode(parseDecls(brand.light), parseDecls(brand.dark))
    const diffs = []
    for (const [name, value] of merged) {
      if (value !== base.get(name)) diffs.push([name, value])
    }
    o.push(`[data-theme="${brand.attr}"] {`)
    for (const [name, value] of diffs) o.push(`  ${name}: ${value};`)
    o.push("}")
    o.push("")
    console.log(`✓ brand "${brand.attr}": ${diffs.length} override tokens`)
  }

  writeFileSync(OVERRIDES_OUT, o.join("\n"))
  console.log(`✓ wrote ${OVERRIDES_OUT}`)
}

main()
