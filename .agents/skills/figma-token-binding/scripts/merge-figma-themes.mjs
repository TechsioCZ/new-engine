#!/usr/bin/env node
/*
 * Merge the per-mode Figma exports into a single Tailwind-consumable file.
 *
 * Inputs:
 *   libs/ui/src/tokens/figma/light/variables.css   (:root { … })
 *   libs/ui/src/tokens/figma/dark/variables.css    (:root { … })
 *
 * Output:
 *   libs/ui/src/tokens/figma/variables.css         (@theme static { … })
 *
 * Per-token rule:
 *   - Same value in both modes → emit `--name: <value>;`
 *   - Different values         → emit `--name: light-dark(<L>, <D>);`
 *
 * Tokens declared only in one file are emitted as-is (kept for both modes).
 *
 * The output uses `@theme static` so Tailwind v4 auto-generates utilities
 * from these tokens. Light/dark mode flips happen automatically via
 * `light-dark()` driven by `color-scheme`, which the rest of the token
 * system already manages in _tokens-base.css.
 */

import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..", "..", "..", "..")
const LIGHT = join(REPO_ROOT, "libs/ui/src/tokens/figma/light/variables.css")
const DARK = join(REPO_ROOT, "libs/ui/src/tokens/figma/dark/variables.css")
const OUT = join(REPO_ROOT, "libs/ui/src/tokens/figma/variables.css")

const DECL_RE = /^\s*(--[a-z0-9-]+):\s*([^;]+);/gm

function parseDecls(css) {
  const out = new Map()
  DECL_RE.lastIndex = 0
  for (const m of css.matchAll(DECL_RE)) {
    out.set(m[1], m[2].trim())
  }
  return out
}

function main() {
  const lightDecls = parseDecls(readFileSync(LIGHT, "utf8"))
  const darkDecls = parseDecls(readFileSync(DARK, "utf8"))

  const allNames = new Set([...lightDecls.keys(), ...darkDecls.keys()])
  const sorted = [...allNames].sort()

  let identical = 0
  let mode = 0
  const lines = []
  lines.push("/*")
  lines.push(" * Unified Figma tokens — light + dark merged into a single @theme static.")
  lines.push(" * Generated by merge-figma-themes.mjs. DO NOT EDIT BY HAND.")
  lines.push(" *")
  lines.push(" * - Tokens identical in both modes: single value.")
  lines.push(" * - Tokens that differ:            light-dark(L, D).")
  lines.push(" *")
  lines.push(" * `@theme static` makes Tailwind v4 generate utilities automatically.")
  lines.push(" * `light-dark()` follows the document's `color-scheme`, which the rest of")
  lines.push(" * the token system manages via :is(.dark, .always-dark) selectors.")
  lines.push(" */")
  lines.push("")
  lines.push("@theme static {")

  for (const name of sorted) {
    const L = lightDecls.get(name)
    const D = darkDecls.get(name)
    if (L === undefined) {
      lines.push(`  ${name}: ${D};`)
      identical += 1
      continue
    }
    if (D === undefined || L === D) {
      lines.push(`  ${name}: ${L};`)
      identical += 1
      continue
    }
    // Differ → wrap. If either side already contains light-dark or a
    // long expression, just emit verbatim. The Tailwind preprocessor +
    // browser handle nested light-dark fine (returns inner light-dark
    // result based on color-scheme).
    lines.push(`  ${name}: light-dark(${L}, ${D});`)
    mode += 1
  }

  lines.push("}")
  lines.push("")

  writeFileSync(OUT, lines.join("\n"))
  console.log(`✓ wrote ${OUT}`)
  console.log(`  ${identical} identical, ${mode} mode-different (light-dark)`)
}

main()
