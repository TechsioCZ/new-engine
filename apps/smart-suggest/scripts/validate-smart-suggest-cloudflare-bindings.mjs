#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const workspaceRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const expectedCzVuscCodes = [
  '19',
  '27',
  '35',
  '43',
  '51',
  '60',
  '78',
  '86',
  '94',
  '108',
  '116',
  '124',
  '132',
  '141',
];
const freeTierShardGroups = [
  { index: '01', regionCodes: ['19'] },
  { index: '02', regionCodes: ['27'] },
  { index: '03', regionCodes: ['35', '43'] },
  { index: '04', regionCodes: ['51', '78'] },
  { index: '05', regionCodes: ['60'] },
  { index: '06', regionCodes: ['86', '94', '108'] },
  { index: '07', regionCodes: ['116'] },
  { index: '08', regionCodes: ['124', '132'] },
  { index: '09', regionCodes: ['141'] },
];

function parseArgs(argv) {
  const parsed = {
    app: undefined,
    artifactManifestUrl: undefined,
    artifactStatic: false,
    help: false,
    printCzVuscEnvTemplate: false,
    printFreeTierEnvTemplate: false,
    requireD1: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === '--app') {
      parsed.app = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === '--artifact-static') {
      parsed.artifactStatic = true;
      continue;
    }

    if (arg === '--artifact-manifest-url') {
      parsed.artifactManifestUrl = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === '--print-cz-vusc-env-template') {
      parsed.printCzVuscEnvTemplate = true;
      continue;
    }

    if (arg === '--print-free-tier-env-template') {
      parsed.printFreeTierEnvTemplate = true;
      continue;
    }

    if (arg === '--require-d1') {
      parsed.requireD1 = true;
      continue;
    }

    if (arg === '--apply-migrations') {
      throw new Error(
        '--apply-migrations was removed from this validator. Run explicit Smart Suggest D1 operation scripts instead of mutating generated Cloudflare output.',
      );
    }

    if (arg === '--cz-vusc-shards' || arg === '--compact-cz-shard') {
      throw new Error(
        `${arg} is source-configured by apps/shell-super-app/modern.config.ts. Export the matching SMART_SUGGEST_D1_* env vars before pnpm cloudflare:build.`,
      );
    }

    if (arg === '--help' || arg === '-h') {
      parsed.help = true;
      continue;
    }

    throw new Error(`Unknown argument: ${arg}`);
  }

  if (
    !(parsed.app || parsed.help || parsed.printCzVuscEnvTemplate || parsed.printFreeTierEnvTemplate)
  ) {
    throw new Error('Missing required --app argument.');
  }

  return parsed;
}

function printHelp() {
  process.stdout.write(`Usage:
  node scripts/validate-smart-suggest-cloudflare-bindings.mjs --app shell-super-app [--require-d1] [--artifact-static]
  node scripts/validate-smart-suggest-cloudflare-bindings.mjs --print-free-tier-env-template
  node scripts/validate-smart-suggest-cloudflare-bindings.mjs --print-cz-vusc-env-template

Validates Smart Suggest Cloudflare config generated by UltraModern.
This script is intentionally validation/template-only: it never rewrites
.output/wrangler.json, generated worker bundles, or .output/public files.

Configure D1 bindings and worker vars in source-owned modern.config.ts by exporting
SMART_SUGGEST_D1_* env vars before pnpm cloudflare:build.
`);
}

function envValue(name) {
  const value = process.env[name]?.trim();

  return value === undefined || value === '' ? undefined : value;
}

function normalizeArtifactManifestUrl(args) {
  const explicitUrl =
    args.artifactManifestUrl ?? envValue('SMART_SUGGEST_OWNED_ARTIFACT_MANIFEST_URL');

  if (explicitUrl === undefined) {
    return undefined;
  }

  if (!/^https?:\/\//u.test(explicitUrl)) {
    throw new Error(
      '--artifact-static --artifact-manifest-url must be an absolute HTTP(S) URL when provided.',
    );
  }

  return explicitUrl;
}

function printCzVuscEnvTemplate() {
  const lines = [
    '# Smart Suggest CZ VUSC D1 shard bindings',
    '# Fill database ids from `wrangler d1 create ...` in deployment-owned env, not git.',
    'export SMART_SUGGEST_D1_TOPOLOGY=paid-vusc',
    'export SMART_SUGGEST_D1_SHARD_ROUTE_STRATEGY=vusc',
    'export SMART_SUGGEST_D1_ROUTER_BINDING=SMART_SUGGEST_ROUTER_D1',
    'export SMART_SUGGEST_ROUTER_D1_DATABASE_ID="<router-database-id>"',
    'export SMART_SUGGEST_D1_CZ_VUSC_SHARDS_ENABLED=true',
  ];

  for (const code of expectedCzVuscCodes) {
    lines.push(`export SMART_SUGGEST_CZ_VUSC_${code}_DATABASE_ID="<cz-vusc-${code}-database-id>"`);
  }

  process.stdout.write(`${lines.join('\n')}\n`);
}

function printFreeTierEnvTemplate() {
  const bindingPrefix =
    envValue('SMART_SUGGEST_D1_FREE_TIER_SHARD_BINDING_PREFIX') ?? 'SMART_SUGGEST_FREE_TIER_';
  const shardBindings = freeTierShardGroups.map((group) => `${bindingPrefix}${group.index}`);
  const lines = [
    '# Smart Suggest free-tier D1 bindings',
    '# Uses one router D1 plus 9 row-balanced address D1s; raw snapshots stay outside git.',
    'export SMART_SUGGEST_D1_TOPOLOGY=free-tier',
    'export SMART_SUGGEST_D1_SHARD_ROUTE_STRATEGY=hash',
    'export SMART_SUGGEST_D1_ROUTER_BINDING=SMART_SUGGEST_ROUTER_D1',
    'export SMART_SUGGEST_ROUTER_D1_DATABASE_ID="<router-database-id>"',
    'export SMART_SUGGEST_D1_FREE_TIER_MAX_SHARDS_ENABLED=true',
  ];

  for (const binding of shardBindings) {
    lines.push(
      `export ${binding}_DATABASE_ID="<${binding.toLowerCase().replaceAll('_', '-')}-database-id>"`,
    );
  }

  lines.push(`export SMART_SUGGEST_D1_SHARD_BINDINGS="${shardBindings.join(',')}"`);

  process.stdout.write(`${lines.join('\n')}\n`);
}

function assertInside(parent, child) {
  if (child !== parent && !child.startsWith(`${parent}${path.sep}`)) {
    throw new Error(`Path escaped expected root: ${child}`);
  }
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function assertNoServerOnlyPublicOutput(appRoot) {
  const publicRoot = path.join(appRoot, '.output/public');
  const forbidden = ['api', 'shared']
    .map((entry) => path.join(publicRoot, entry))
    .filter((entryPath) => fs.existsSync(entryPath));

  if (forbidden.length > 0) {
    throw new Error(
      `Server-only output leaked into Worker Static Assets: ${forbidden
        .map((entryPath) => path.relative(appRoot, entryPath).split(path.sep).join('/'))
        .join(', ')}. Configure deploy.worker.publicAssetExcludes in modern.config.ts.`,
    );
  }
}

function assertSdkDemoOutput(appRoot) {
  const demoPath = path.join(appRoot, '.output/public/sdk/demo.html');

  if (!fs.existsSync(demoPath)) {
    throw new Error(`Smart Suggest SDK demo missing from generated public assets: ${demoPath}`);
  }
}

function readD1Databases(config) {
  return Array.isArray(config.d1_databases) ? config.d1_databases : [];
}

function assertD1Config(config) {
  const d1Databases = readD1Databases(config);

  if (d1Databases.length === 0) {
    throw new Error(
      'Smart Suggest D1 was required, but generated Wrangler config has no d1_databases. Export SMART_SUGGEST_D1_* env vars before pnpm cloudflare:build.',
    );
  }

  for (const [index, database] of d1Databases.entries()) {
    if (
      typeof database !== 'object' ||
      database === null ||
      typeof database.binding !== 'string' ||
      typeof database.database_name !== 'string' ||
      typeof database.database_id !== 'string'
    ) {
      throw new Error(`Generated d1_databases[${index}] is incomplete.`);
    }
  }
}

function assertArtifactStaticConfig({ args, config }) {
  const vars = config.vars ?? {};
  const expectedManifestUrl = normalizeArtifactManifestUrl(args);

  if (config.assets?.html_handling !== 'none') {
    throw new Error(
      'Cloudflare static asset html_handling must be none for explicit SDK demo URLs.',
    );
  }

  if (vars.SMART_SUGGEST_OWNED_ARTIFACT_ALLOW_INCOMPLETE !== 'false') {
    throw new Error('SMART_SUGGEST_OWNED_ARTIFACT_ALLOW_INCOMPLETE must be false.');
  }

  if (vars.SMART_SUGGEST_OWNED_ARTIFACT_READ_FALLBACK_ADDRESS_RECORDS !== 'false') {
    throw new Error('SMART_SUGGEST_OWNED_ARTIFACT_READ_FALLBACK_ADDRESS_RECORDS must be false.');
  }

  if (
    expectedManifestUrl !== undefined &&
    vars.SMART_SUGGEST_OWNED_ARTIFACT_MANIFEST_URL !== expectedManifestUrl
  ) {
    throw new Error(
      `SMART_SUGGEST_OWNED_ARTIFACT_MANIFEST_URL mismatch. Expected ${expectedManifestUrl}, generated ${vars.SMART_SUGGEST_OWNED_ARTIFACT_MANIFEST_URL}.`,
    );
  }
}

function main(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);

  if (args.help) {
    printHelp();
    return 0;
  }

  if (args.printCzVuscEnvTemplate) {
    printCzVuscEnvTemplate();
    return 0;
  }

  if (args.printFreeTierEnvTemplate) {
    printFreeTierEnvTemplate();
    return 0;
  }

  const appsRoot = path.resolve(workspaceRoot, 'apps');
  const appRoot = path.resolve(appsRoot, args.app);
  assertInside(appsRoot, appRoot);

  const wranglerPath = path.join(appRoot, '.output/wrangler.json');

  if (!fs.existsSync(wranglerPath)) {
    throw new Error(
      `Generated Wrangler config missing: ${wranglerPath}. Run app Cloudflare build first.`,
    );
  }

  const config = readJson(wranglerPath);

  assertSdkDemoOutput(appRoot);
  assertNoServerOnlyPublicOutput(appRoot);

  if (args.artifactStatic) {
    assertArtifactStaticConfig({ args, config });
  }

  if (args.requireD1) {
    assertD1Config(config);
  }

  process.stdout.write(`Validated Smart Suggest Cloudflare config ${wranglerPath}\n`);
  process.stdout.write('Verified SDK demo output at sdk/demo.html\n');

  return 0;
}

try {
  process.exitCode = main();
} catch (error) {
  process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
}
